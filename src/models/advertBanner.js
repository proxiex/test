const mongoose = require('mongoose');

const adBannerSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  link: String,
  status: {
    type: String,
    enum: ['active', 'inactive'],
    default: 'active',
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const AdBanner = mongoose.model('AdBanner', adBannerSchema);

module.exports = AdBanner;
